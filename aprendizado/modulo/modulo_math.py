import math

# Acessar o número PI
print(math.pi)
print(f"{math.pi:.2f}")

# Acessar o número de Euler
print(math.e)
print(f'{math.e:.2f}')

# Arredondar números para cima e para baixo
num = 10.4
print(math.ceil(num))
print(math.floor(num))

# Fatorial de número
print(math.factorial(5))

# Potência de números
print(math.pow(5, 5))

# Raiz quadrada
print(math.sqrt(169))

# Máximo Divisor Comum - MDC
print(math.gcd(20, 100))

# Logaritmo
print(math.log(10))




