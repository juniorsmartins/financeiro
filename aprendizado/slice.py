movieName = "Top_Gun"

# Buscar toda a string a partir da primeira posição
print(movieName[0:])

# Buscar toda a string a partir da posição 4
print(movieName[4:])

# Buscar toda string até a posição 6
print(movieName[:6])

# Buscar toda string de dois em dois caracteres - string[início:fim:passo]
print(movieName[::2])
print(movieName[0:6:2])

# Buscar toda string nos índices ímpares
print(movieName[1::2])

# Inverter uma string de trás pra frente
print(movieName[::-1])

